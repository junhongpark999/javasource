package protice1;

public class Person {
	String name;
	float height;
	float weight;
	
	public Person() {
		super();
	}

	public Person(String pname) {
		this.name = pname;
	}
	public Person(String name, float height, float weight) {
		super();
		this.name = name;
		this.height = height;
		this.weight = weight;


		
	}	
	
}
